def generate_training_pairs(sentences, window_size=2):
    """Генерирует пары (центр, контекст) для обучения."""

    pairs = []

    for sent in sentences:
        for i, center_word in enumerate(sent):
            # Определяем границы окна
            start = max(0, i - window_size)
            end = min(len(sent), i + window_size + 1)
            
            # Берём контекстные слова
            for j in range(start, end):
                if j == i:
                    continue  # пропускаем само слово
                context_word = sent[j]
                pairs.append((center_word, context_word))

    return pairs