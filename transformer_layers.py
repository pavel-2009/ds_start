"""Подробно прокомментированные низкоуровневые компоненты трансформера."""

# Импортируем NumPy, потому что все операции внимания, нормализации и линейных слоев выражаются через массивы.
import numpy as np


# Определяем стабильную версию softmax, которая используется в механизме внимания.
def softmax(x, axis=-1):
    # Вычитаем максимум вдоль указанной оси, чтобы экспонента не переполнялась на больших значениях.
    exp_x = np.exp(x - np.max(x, axis=axis, keepdims=True))
    # Нормируем экспоненты их суммой, получая вероятностное распределение вдоль нужной оси.
    return exp_x / np.sum(exp_x, axis=axis, keepdims=True)


# Реализуем масштабированное скалярное внимание как базовый строительный блок трансформера.
def scaled_dot_product_attention(q: np.ndarray, k: np.ndarray, v: np.ndarray, mask: np.ndarray = None) -> tuple[np.ndarray, np.ndarray]:
    # Вычисляем матрицу совместимости запросов и ключей и делим на корень из размерности головы для стабилизации масштаба.
    scaled_scores = np.matmul(q, k.transpose(-2, -1)) / np.sqrt(q.shape[-1])
    # Если маска передана, добавляем ее к оценкам совместимости, чтобы запретить внимание к нежелательным позициям.
    if mask is not None:
        scaled_scores = scaled_scores + mask
    # Преобразуем оценки в веса внимания через softmax, чтобы получить коэффициенты суммирования значений.
    attention_weights = softmax(scaled_scores, axis=-1)
    # Умножаем веса внимания на значения, чтобы получить взвешенную сумму контекстной информации.
    output = np.matmul(attention_weights, v)
    # Возвращаем и итоговые представления, и сами веса внимания, потому что оба результата полезны для последующих шагов.
    return output, attention_weights


# Объявляем класс многоголового внимания, который параллельно смотрит на данные несколькими независимыми подпространствами.
class MultiHeadAttention:
    # Определяем конструктор, чтобы задать размерность модели и число голов внимания.
    def __init__(self, d_model: int, num_heads: int):
        # Проверяем, что размерность модели делится на число голов без остатка, иначе разбиение на головы невозможно.
        assert d_model % num_heads == 0, 'd_model должен быть кратным num_heads'
        # Сохраняем общую размерность модели, чтобы использовать ее при проекциях и сборке голов.
        self.d_model = d_model
        # Сохраняем число голов внимания как архитектурный параметр.
        self.num_heads = num_heads
        # Вычисляем глубину одной головы как долю общей размерности модели.
        self.depth = d_model // num_heads
        # Инициализируем матрицу проекции запросов случайными значениями, сохраняя исходную простую схему.
        self.wq = np.random.rand(d_model, d_model)
        # Инициализируем матрицу проекции ключей.
        self.wk = np.random.rand(d_model, d_model)
        # Инициализируем матрицу проекции значений.
        self.wv = np.random.rand(d_model, d_model)
        # Инициализируем матрицу финальной проекции после объединения голов.
        self.wo = np.random.rand(d_model, d_model)

    # Вспомогательный метод разбивает последнюю размерность на несколько голов внимания.
    def _split_heads(self, x: np.ndarray) -> np.ndarray:
        # Считываем размер батча, потому что он нужен при reshape и должен сохраниться неизменным.
        batch_size = x.shape[0]
        # Преобразуем тензор в форму (batch, seq, heads, depth), а затем переставляем оси для удобства вычисления внимания.
        return x.reshape(batch_size, -1, self.num_heads, self.depth).transpose(0, 2, 1, 3)

    # Вспомогательный метод собирает несколько голов обратно в общую размерность модели.
    def _combine_heads(self, x: np.ndarray) -> np.ndarray:
        # Считываем размер батча, потому что его нужно сохранить при обратном reshape.
        batch_size = x.shape[0]
        # Возвращаем оси в порядок (batch, seq, heads, depth) и сливаем головы в размерность d_model.
        return x.transpose(0, 2, 1, 3).reshape(batch_size, -1, self.d_model)

    # Определяем прямой проход через многоголовое внимание.
    def forward(self, q: np.ndarray, k: np.ndarray, v: np.ndarray, mask: np.ndarray = None) -> tuple[np.ndarray, np.ndarray]:
        # Выполняем линейную проекцию запросов в пространство модели.
        q = np.matmul(q, self.wq)
        # Выполняем линейную проекцию ключей.
        k = np.matmul(k, self.wk)
        # Выполняем линейную проекцию значений.
        v = np.matmul(v, self.wv)
        # Разбиваем запросы на несколько голов, чтобы каждая голова работала с собственной частью признаков.
        q = self._split_heads(q)
        # Разбиваем ключи на головы.
        k = self._split_heads(k)
        # Разбиваем значения на головы.
        v = self._split_heads(v)
        # Вычисляем внимание независимо для каждой головы.
        attention_output, attention_weights = scaled_dot_product_attention(q, k, v, mask)
        # Собираем выходы голов обратно в единый тензор размерности модели.
        attention_output = self._combine_heads(attention_output)
        # Пропускаем объединенный результат через финальную линейную проекцию.
        output = np.matmul(attention_output, self.wo)
        # Возвращаем и итоговые представления, и веса внимания.
        return output, attention_weights


# Объявляем класс позиционно-независимой feed-forward сети, которая обрабатывает каждый токен отдельно.
class FeedForwardNetwork:
    # Определяем конструктор, чтобы задать размерность модели и внутреннего скрытого слоя.
    def __init__(self, d_model: int, d_ff: int):
        # Сохраняем размерность модели как размер входа и выхода сети.
        self.d_model = d_model
        # Сохраняем размерность скрытого слоя, определяющую выразительность нелинейного преобразования.
        self.d_ff = d_ff
        # Инициализируем веса первого линейного слоя.
        self.w1 = np.random.rand(d_model, d_ff)
        # Инициализируем смещения первого слоя.
        self.b1 = np.random.rand(d_ff)
        # Инициализируем веса второго линейного слоя.
        self.w2 = np.random.rand(d_ff, d_model)
        # Инициализируем смещения второго слоя.
        self.b2 = np.random.rand(d_model)

    # Определяем прямой проход через два линейных преобразования с ReLU между ними.
    def forward(self, x: np.ndarray) -> np.ndarray:
        # Применяем первый линейный слой, расширяя представление в скрытое пространство.
        x = np.matmul(x, self.w1) + self.b1
        # Применяем ReLU, чтобы сделать преобразование нелинейным.
        x = np.maximum(0, x)
        # Применяем второй линейный слой, возвращая представление к размерности модели.
        x = np.matmul(x, self.w2) + self.b2
        # Возвращаем преобразованный тензор.
        return x


# Объявляем класс layer normalization, которая нормализует признаки внутри каждого токена.
class LayerNormalization:
    # Определяем конструктор с размерностью модели и маленькой константой для численной устойчивости.
    def __init__(self, d_model: int, epsilon: float = 1e-6):
        # Сохраняем размерность модели, чтобы правильно задать форму обучаемых параметров.
        self.d_model = d_model
        # Сохраняем epsilon, чтобы не делить на ноль при очень маленьком стандартном отклонении.
        self.epsilon = epsilon
        # Создаем вектор масштабирования gamma и инициализируем его единицами, чтобы изначально не менять масштаб признаков.
        self.gamma = np.ones(d_model)
        # Создаем вектор смещения beta и инициализируем его нулями, чтобы изначально не сдвигать признаки.
        self.beta = np.zeros(d_model)

    # Определяем прямой проход слоя нормализации.
    def forward(self, x: np.ndarray) -> np.ndarray:
        # Считаем среднее по последней оси, то есть по признакам каждого токена.
        mean = np.mean(x, axis=-1, keepdims=True)
        # Считаем стандартное отклонение по той же оси, чтобы измерить разброс признаков.
        std = np.std(x, axis=-1, keepdims=True)
        # Нормализуем признаки к нулевому среднему и единичному масштабу с учетом epsilon.
        normalized_x = (x - mean) / (std + self.epsilon)
        # Применяем обучаемое масштабирование и смещение, позволяя модели вернуть нужный диапазон значений.
        output = self.gamma * normalized_x + self.beta
        # Возвращаем нормализованный тензор.
        return output


# Определяем синусоидальное позиционное кодирование, чтобы модель знала порядок токенов.
def positional_encoding(seq_len: int, d_model: int) -> np.ndarray:
    # Создаем столбец позиций от нуля до длины последовательности минус один.
    position = np.arange(seq_len)[:, np.newaxis]
    # Вычисляем знаменатели частот для четных координат размерности модели.
    div_term = np.exp(np.arange(0, d_model, 2) * -(np.log(10000.0) / d_model))
    # Создаем нулевую матрицу, в которую запишем синусы и косинусы для каждой позиции.
    pe = np.zeros((seq_len, d_model))
    # Заполняем четные столбцы синусами, кодируя позицию гладкой периодической функцией.
    pe[:, 0::2] = np.sin(position * div_term)
    # Заполняем нечетные столбцы косинусами, получая вторую фазу позиционного сигнала.
    pe[:, 1::2] = np.cos(position * div_term)
    # Возвращаем готовую матрицу позиционного кодирования.
    return pe
