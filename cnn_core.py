"""Подробно прокомментированная базовая реализация сверточной нейронной сети."""

# Импортируем NumPy, потому что вся математика в этом учебном примере строится на массивах и векторных операциях.
import numpy as np


# Объявляем основной класс CNN, который инкапсулирует параметры модели и шаги прямого/обратного прохода.
class CNN:
    # Определяем конструктор класса, чтобы при создании объекта сразу настроить архитектуру и гиперпараметры.
    def __init__(
        self,
        num_classes: int,
        num_filters: int,
        filter_size: int,
        input_shape: tuple,
        learning_rate: float = 0.01,
    ):
        # Сохраняем количество классов, потому что выходной слой должен выдавать по одному логиту на каждый класс.
        self.num_classes = num_classes
        # Сохраняем число фильтров, потому что именно столько карт признаков сеть будет строить после свертки.
        self.num_filters = num_filters
        # Сохраняем размер свертки, чтобы одинаково использовать его и при инициализации, и при проходе по изображению.
        self.filter_size = filter_size
        # Сохраняем форму входа, чтобы приводить входные данные к ожидаемому размеру.
        self.input_shape = input_shape
        # Сохраняем скорость обучения, потому что этот коэффициент регулирует размер шага при обновлении параметров.
        self.learning_rate = learning_rate
        # Создаем пустой список фильтров; реальные значения фильтров будут добавлены отдельным методом инициализации.
        self.filters = []
        # Вызываем инициализацию фильтров сразу в конструкторе, чтобы модель была готова к использованию после создания.
        self.create_filters()
        # Создаем матрицу весов полносвязного слоя малого масштаба, чтобы после свертки и разворачивания получить логиты классов.
        self.dense_weights = np.random.rand(
            num_filters * (input_shape[0] - filter_size + 1) * (input_shape[1] - filter_size + 1),
            num_classes,
        ) * 0.1
        # Создаем вектор смещений для полносвязного слоя и инициализируем его нулями для простоты и стабильности старта.
        self.dense_bias = np.zeros(num_classes)

    # Определяем прямой проход, который принимает вход и возвращает логиты до softmax.
    def forward(self, X: np.ndarray, target: np.ndarray = None) -> np.ndarray:
        # Приводим вход к ожидаемой двумерной форме изображения, чтобы последующие операции свертки работали корректно.
        X = X.reshape(self.input_shape)
        # Создаем список, в который будем складывать карты признаков от каждого фильтра.
        outputs = []
        # Последовательно применяем каждый фильтр к изображению, чтобы построить набор признаков.
        for current_filter in self.filters:
            # Выполняем дискретную свертку текущего фильтра с входным изображением.
            output = self.convolve(X, current_filter)
            # Пропускаем результат через ReLU, чтобы убрать отрицательные значения и внести нелинейность.
            output = self.relu(output)
            # Сохраняем карту признаков, чтобы позже объединить карты от всех фильтров.
            outputs.append(output)
        # Собираем отдельные карты признаков в единый тензор формы (количество фильтров, ширина, высота).
        conv_output = np.stack(outputs)
        # Разворачиваем тензор признаков в один длинный вектор, потому что полносвязный слой ожидает плоский вход.
        flattened = conv_output.flatten()
        # Вычисляем логиты классов как линейное преобразование признаков с добавлением смещений.
        logits = np.dot(flattened, self.dense_weights) + self.dense_bias
        # Возвращаем логиты; softmax и функция потерь вызываются отдельно, чтобы логика оставалась модульной.
        return logits

    # Определяем функцию ReLU как базовую нелинейность для сверточных карт признаков.
    def relu(self, X: np.ndarray) -> np.ndarray:
        # Возвращаем поэлементный максимум между нулем и входом, чтобы оставить только положительные активации.
        return np.maximum(0, X)

    # Определяем производную ReLU, которая нужна при обратном распространении ошибки.
    def relu_derivative(self, X: np.ndarray) -> np.ndarray:
        # Для положительных элементов производная равна 1, а для остальных 0; преобразуем булев результат в числа с плавающей точкой.
        return (X > 0).astype(float)

    # Определяем softmax, чтобы превращать логиты в вероятности классов.
    def softmax(self, X: np.ndarray) -> np.ndarray:
        # Вычитаем максимум из логитов для численной стабильности и предотвращения переполнения экспоненты.
        exp_X = np.exp(X - np.max(X))
        # Делим каждую экспоненту на сумму всех экспонент, чтобы получить нормированное распределение вероятностей.
        return exp_X / np.sum(exp_X)

    # Определяем функцию потерь кросс-энтропии для задачи многоклассовой классификации.
    def compute_loss(self, output: np.ndarray, target: np.ndarray) -> float:
        # Сначала переводим логиты в вероятности классов через softmax.
        probs = self.softmax(output)
        # Считаем отрицательное логарифмическое правдоподобие правильного класса; маленькая константа защищает от log(0).
        loss = -np.sum(target * np.log(probs + 1e-15))
        # Возвращаем скалярное значение потерь, чтобы использовать его в мониторинге обучения.
        return loss

    # Вычисляем размер входа в полносвязный слой, исходя из геометрии свертки.
    def compute_dense_input_size(self) -> np.ndarray:
        # Определяем ширину выходной карты признаков после свертки без паддинга и со шагом 1.
        output_width = self.input_shape[0] - self.filter_size + 1
        # Определяем высоту выходной карты признаков по той же формуле.
        output_height = self.input_shape[1] - self.filter_size + 1
        # Умножаем ширину, высоту и число фильтров, потому что после разворачивания все карты объединяются в один вектор.
        return self.num_filters * output_width * output_height

    # Определяем обратный проход, который обновляет веса полносвязного слоя и фильтры свертки.
    def backward(self, d_logits: np.ndarray, conv_output: np.ndarray, X: np.ndarray):
        # Разворачиваем сверточный выход в вектор, потому что именно в таком виде он подавался на полносвязный слой.
        flattened = conv_output.flatten()
        # Сначала переносим градиент с логитов назад на вход полносвязного слоя, пока веса еще не изменены текущим шагом.
        d_flattened = np.dot(d_logits, self.dense_weights.T)
        # Обновляем матрицу весов полносвязного слоя через внешний продукт входных признаков и градиента по логитам.
        self.dense_weights -= self.learning_rate * np.outer(flattened, d_logits)
        # Обновляем смещения полносвязного слоя, сдвигая их в сторону уменьшения функции потерь.
        self.dense_bias -= self.learning_rate * d_logits
        # Возвращаем градиент к форме сверточного тензора, чтобы продолжить обратный проход через карты признаков.
        d_conv_output = d_flattened.reshape(conv_output.shape)
        # Учитываем производную ReLU, потому что через неактивные нейроны градиент проходить не должен.
        d_conv = d_conv_output * self.relu_derivative(conv_output)
        # Создаем копию градиента по сверточным выходам, чтобы отдельно использовать его для каждого фильтра.
        d_out = d_conv.copy()
        # Гарантируем правильную форму входного изображения для извлечения локальных областей при вычислении градиентов фильтров.
        X = X.reshape(self.input_shape)
        # Проходим по всем фильтрам, потому что каждый фильтр получает собственный градиент.
        for filter_idx, current_filter in enumerate(self.filters):
            # Создаем массив нулей той же формы, что и фильтр, чтобы постепенно накопить его градиент.
            d_filter = np.zeros_like(current_filter)
            # Проходим по всем позициям выходной карты признаков по вертикали.
            for i in range(d_out.shape[1]):
                # Проходим по всем позициям выходной карты признаков по горизонтали.
                for j in range(d_out.shape[2]):
                    # Вырезаем тот фрагмент входного изображения, который соответствовал текущей позиции свертки.
                    region = X[i:i + self.filter_size, j:j + self.filter_size]
                    # Добавляем вклад текущей позиции в общий градиент фильтра.
                    d_filter += d_out[filter_idx, i, j] * region
            # Обновляем текущий фильтр градиентным шагом, уменьшая функцию потерь.
            self.filters[filter_idx] -= self.learning_rate * d_filter

    # Определяем свертку как проход фильтра по всем локальным областям изображения.
    def convolve(self, X: np.ndarray, current_filter: np.ndarray) -> np.ndarray:
        # Извлекаем ширину и высоту входного изображения из заранее сохраненной формы входа.
        width, height = self.input_shape
        # Считаем ширину карты признаков после свертки без паддинга.
        output_width = width - self.filter_size + 1
        # Считаем высоту карты признаков по той же формуле.
        output_height = height - self.filter_size + 1
        # Создаем пустой выходной массив, куда будем записывать результаты скалярных произведений.
        output = np.zeros((output_width, output_height))
        # Проходим по всем вертикальным координатам карты признаков.
        for i in range(output_width):
            # Проходим по всем горизонтальным координатам карты признаков.
            for j in range(output_height):
                # Вырезаем локальный участок изображения размера фильтра.
                region = X[i:i + self.filter_size, j:j + self.filter_size]
                # Вычисляем значение свертки как сумму поэлементного произведения области и фильтра.
                output[i, j] = np.sum(region * current_filter)
        # Возвращаем построенную карту признаков для текущего фильтра.
        return output

    # Определяем инициализацию фильтров как создание набора маленьких случайных матриц.
    def create_filters(self):
        # Повторяем процесс столько раз, сколько фильтров запросил пользователь архитектуры.
        for _ in range(self.num_filters):
            # Создаем маленький случайный фильтр, чтобы стартовые значения не были слишком большими.
            current_filter = np.random.rand(self.filter_size, self.filter_size) * 0.1
            # Добавляем фильтр в список параметров модели.
            self.filters.append(current_filter)
