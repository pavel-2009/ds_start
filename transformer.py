"""
Кастомный трансформер для обработки данных в рамках проекта по анализу текстов.
"""

import numpy as np


def softmax(x, axis=-1):
    """Стабильный softmax"""
    exp_x = np.exp(x - np.max(x, axis=axis, keepdims=True))
    return exp_x / np.sum(exp_x, axis=axis, keepdims=True)


def scaled_dot_product_attention(q: np.ndarray, k: np.ndarray, v: np.ndarray, mask: np.ndarray = None) -> tuple[np.ndarray, np.ndarray]:
    """
    Вычисление внимания с помощью масштабированного скаляра
    
    Получает на вход запросы (q), ключи (k) и значения (v), а также необязательную маску.
    Возвращает выходные векторы и веса внимания.
    По сути, на выходе мы получаем взвешенную сумму значений, где веса определяются совместимостью между запросами и ключами.
    После - используем эти веса как коэффициенты для суммирования значений, что позволяет модели фокусироваться на наиболее релевантных частях входных данных.
    """
    
    # Вычисление совместимости между запросами и ключами
    scaled_scores = np.matmul(q, k.transpose(-2, -1)) / np.sqrt(q.shape[-1])
    
    # Применение маски, если она предоставлена
    if mask is not None:
        scaled_scores = scaled_scores + mask
    
    # Применение softmax для получения весов внимания
    attention_weights = softmax(scaled_scores, axis=-1)
    
    # Вычисление выходного вектора как взвешенной суммы значений
    output = np.matmul(attention_weights, v)
    
    return output, attention_weights
    
    
class MultiHeadAttention:
    """
    Класс для реализации механизма многоголового внимания (Multi-Head Attention).
    Каждая голова внимания позволяет модели фокусироваться на разных аспектах входных данных, что улучшает способность модели захватывать сложные зависимости в тексте.
    Каждая голова отдельно обрабатывает входные данные, а затем результаты объединяются для получения более богатого представления.
    По сути - мы получаем несколько различных матриц внимания, как в функции scaled_dot_product_attention, и объединяем их для получения более комплексного понимания входных данных.
    """

    def __init__(self, d_model: int, num_heads: int):
        assert d_model % num_heads == 0, "d_model должен быть кратным num_heads"
        
        self.d_model = d_model
        self.num_heads = num_heads
        self.depth = d_model // num_heads
        
        # Инициализация весов для линейных преобразований запросов, ключей и значений
        self.wq = np.random.rand(d_model, d_model)
        self.wk = np.random.rand(d_model, d_model)
        self.wv = np.random.rand(d_model, d_model)
        
        # Инициализация весов для объединения голов внимания
        self.wo = np.random.rand(d_model, d_model)
        
        
    def _split_heads(self, x: np.ndarray) -> np.ndarray:
        """
        Разделение на головы.
        На входе получаем результат функции линейного преобразования, который имеет форму (batch_size, seq_len, d_model).
        Это - три матрицы для запросов, ключей и значений, которые мы будем использовать для вычисления внимания.
        Мы разделяем эти матрицы на num_heads частей, каждая из которых имеет размер depth (глубина каждой головы внимания), и затем транспонируем их для удобства дальнейших вычислений.
        """
        batch_size = x.shape[0]
        return x.reshape(batch_size, -1, self.num_heads, self.depth).transpose(0, 2, 1, 3)
    
    
    def _combine_heads(self, x: np.ndarray) -> np.ndarray:
        """
        Объединение голов.
        На входе получаем результат функции scaled_dot_product_attention, который имеет форму (batch_size, num_heads, seq_len, depth).
        Мы транспонируем его обратно и объединяем все головы в одну матрицу с размером (batch_size, seq_len, d_model), что позволяет нам получить итоговое представление после применения внимания.
        """
        batch_size = x.shape[0]
        return x.transpose(0, 2, 1, 3).reshape(batch_size, -1, self.d_model)
    
    
    def forward(self, q: np.ndarray, k: np.ndarray, v: np.ndarray, mask: np.ndarray = None) -> tuple[np.ndarray, np.ndarray]:
        """
        Прямой проход через многоголовое внимание.
        На входе получаем запросы (q), ключи (k) и значения (v), а также необязательную маску.
        Мы применяем линейные преобразования к запросам, ключам и значениям, затем разделяем их на головы, вычисляем внимание для каждой головы, объединяем результаты и применяем финальное линейное преобразование для получения итогового представления.
        Это итоговое представление, по сути, является результатом того, как модель фокусируется на различных аспектах входных данных, что позволяет ей захватывать сложные зависимости и улучшать понимание текста.
        В дальнейшем это представление может быть использовано в других компонентах трансформера, таких как слои нормализации и feed-forward сети, для дальнейшей обработки и генерации выходных данных.
        """
        batch_size = q.shape[0]
        
        # Линейные преобразования
        q = np.matmul(q, self.wq)
        k = np.matmul(k, self.wk)
        v = np.matmul(v, self.wv)
        
        # Разделение на головы
        q = self._split_heads(q)
        k = self._split_heads(k)
        v = self._split_heads(v)
        
        # Вычисление внимания для каждой головы
        attention_output, attention_weights = scaled_dot_product_attention(q, k, v, mask)
        
        # Объединение голов
        attention_output = self._combine_heads(attention_output)
        
        # Финальное линейное преобразование
        output = np.matmul(attention_output, self.wo)
        
        return output, attention_weights
        
        
class FeedForwardNetwork:
    """
    Класс для реализации позиционно-независимой feed-forward сети.
    Этот компонент используется в трансформере для обработки выходных данных после применения внимания, позволяя модели захватывать более сложные зависимости и улучшать представление данных.
    Сеть состоит из двух линейных слоев с активацией ReLU между ними, что позволяет модели учиться нелинейным зависимостям в данных.
    По сути, эта сеть помогает модели обрабатывать информацию, которая была выделена механизмом внимания, и улучшать способность модели к генерализации и пониманию текста.
    """

    def __init__(self, d_model: int, d_ff: int):
        self.d_model = d_model
        self.d_ff = d_ff
        
        # Инициализация весов для двух линейных слоев
        self.w1 = np.random.rand(d_model, d_ff)
        self.b1 = np.random.rand(d_ff)
        self.w2 = np.random.rand(d_ff, d_model)
        self.b2 = np.random.rand(d_model)
        
        
    def forward(self, x: np.ndarray) -> np.ndarray:
        """
        Прямой проход через feed-forward сеть.
        На входе получаем матрицу с размером (batch_size, seq_len, d_model), которая является выходом механизма внимания.
        Мы применяем первый линейный слой с активацией ReLU, а затем второй линейный слой для получения итогового представления.
        Это итоговое представление позволяет модели захватывать более сложные зависимости в данных и улучшать способность к генерализации и пониманию текста.
        В дальнейшем это представление может быть использовано в других компонентах трансформера для дальнейшей обработки и генерации выходных данных.
        """
        # Первый линейный слой с активацией ReLU
        x = np.matmul(x, self.w1) + self.b1
        x = np.maximum(0, x)  # ReLU
        
        # Второй линейный слой
        x = np.matmul(x, self.w2) + self.b2
        
        return x
    
    
class LayerNormalization:
    """
    Класс для реализации слоя нормализации.
    Этот компонент используется в трансформере для стабилизации и ускорения обучения, а также для улучшения способности модели к генерализации.
    Слой нормализации нормализует входные данные по каждому примеру в батче, что помогает модели лучше справляться с изменениями в распределении данных и улучшать способность к генерализации.
    По сути, этот слой помогает модели обрабатывать данные более эффективно и стабильно, что может привести к улучшению производительности и способности к генерализации.
    """
    
    def __init__(self, d_model: int, epsilon: float = 1e-6):
        self.d_model = d_model
        self.epsilon = epsilon
        
        # Инициализация параметров для масштабирования и смещения
        self.gamma = np.ones(d_model)
        self.beta = np.zeros(d_model)
        
        
    def forward(self, x: np.ndarray) -> np.ndarray:
        """
        Прямой проход через слой нормализации.
        На входе получаем матрицу с размером (batch_size, seq_len, d_model), которая является выходом механизма внимания или feed-forward сети.
        Мы вычисляем среднее и стандартное отклонение по последнему измерению (d_model) и нормализуем данные, а затем применяем масштабирование и смещение для получения итогового представления.
        Это итоговое представление позволяет модели обрабатывать данные более эффективно и стабильно, что может привести к улучшению производительности и способности к генерализации.
        В дальнейшем это представление может быть использовано в других компонентах трансформера для дальнейшей обработки и генерации выходных данных.
        """
        # Вычисление среднего и стандартного отклонения по последнему измерению
        mean = np.mean(x, axis=-1, keepdims=True)
        std = np.std(x, axis=-1, keepdims=True)
        
        # Нормализация данных
        normalized_x = (x - mean) / (std + self.epsilon)
        
        # Применение масштабирования и смещения
        # gamma - это параметр масштабирования, который позволяет модели регулировать масштаб нормализованных данных, а beta - это параметр смещения, который позволяет модели регулировать смещение нормализованных данных.
        # Эти параметры обучаются во время тренировки модели и помогают ей адаптироваться к различным распределениям данных, что улучшает способность к генерализации и производительность модели.
        # Эти параметры изначально рандомно инициализируются, но в процессе обучения они оптимизируются для достижения наилучших результатов на конкретной задаче.
        output = self.gamma * normalized_x + self.beta
        
        return output
    
    
def positional_encoding(seq_len: int, d_model: int) -> np.ndarray:
    """
    Функция для генерации позиционного кодирования.
    Позиционное кодирование добавляет информацию о позиции токенов в последовательности, что позволяет модели учитывать порядок слов в тексте.
    Это особенно важно для трансформера, который не имеет рекуррентных или сверточных слоев и не может напрямую обрабатывать последовательности.
    По сути, позиционное кодирование позволяет модели захватывать информацию о порядке слов в тексте, что улучшает способность к пониманию и генерализации.
    """
    # Генерация позиционного кодирования с помощью синусоидальных функций
    position = np.arange(seq_len)[:, np.newaxis]
    div_term = np.exp(np.arange(0, d_model, 2) * -(np.log(10000.0) / d_model))
    
    # Инициализация матрицы позиционного кодирования и заполнение ее значениями
    # Значения изначально инициализируются нулями, а затем заполняются синусоидальными функциями для четных индексов и косинусоидальными функциями для нечетных индексов, что позволяет модели захватывать информацию о позиции токенов в последовательности.
    pe = np.zeros((seq_len, d_model))
    pe[:, 0::2] = np.sin(position * div_term)
    pe[:, 1::2] = np.cos(position * div_term)
    
    return pe


class Encoder:
    """
    Класс для реализации кодировщика трансформера.
    Кодировщик состоит из нескольких слоев, каждый из которых включает в себя механизм многоголового внимания, позиционно-независимую feed-forward сеть и слой нормализации.
    Схема:
    Входные данные -> Многоголовое внимание -> Слой нормализации -> Feed-forward сеть -> Слой нормализации -> Выходные данные
    """
    
    def __init__(self, num_layers: int, d_model: int, num_heads: int, d_ff: int):
        self.num_layers = num_layers
        self.d_model = d_model
        
        # Инициализация слоев кодировщика
        self.attention_layers = [MultiHeadAttention(d_model, num_heads) for _ in range(num_layers)]
        self.ffn_layers = [FeedForwardNetwork(d_model, d_ff) for _ in range(num_layers)]
        self.norm_layers = [LayerNormalization(d_model) for _ in range(num_layers)]
        
        
    def forward(self, x: np.ndarray, mask: np.ndarray = None) -> np.ndarray:
        """
        Прямой проход через кодировщик.
        На входе получаем матрицу с размером (batch_size, seq_len, d_model), которая является входными данными для кодировщика, а также необязательную маску.
        Мы последовательно проходим через каждый слой кодировщика, применяя механизм многоголового внимания, слой нормализации, feed-forward сеть и снова слой нормализации для получения итогового представления.
        Это итоговое представление позволяет модели захватывать сложные зависимости в данных и улучшать способность к генерализации и пониманию текста.
        В дальнейшем это представление может быть использовано в других компонентах трансформера для дальнейшей обработки и генерации выходных данных.
        """
        for i in range(self.num_layers):
            # Многоголовое внимание
            attention_output, _ = self.attention_layers[i].forward(x, x, x, mask)
            x = self.norm_layers[i].forward(x + attention_output)  # Слой нормализации после внимания
            
            # Feed-forward сеть
            ffn_output = self.ffn_layers[i].forward(x)
            x = self.norm_layers[i].forward(x + ffn_output)  # Слой нормализации после feed-forward сети
            
        return x
    
    
class Decoder:
    """
    Класс для реализации декодировщика трансформера.
    Декодировщик состоит из нескольких слоев, каждый из которых включает в себя механизм многоголового внимания, позиционно-независимую feed-forward сеть и слой нормализации.
    Схема:
    Входные данные -> Многоголовое внимание (маскированное) -> Слой нормализации -> Многоголовое внимание (не маскированное) -> Слой нормализации -> Feed-forward сеть -> Слой нормализации -> Выходные данные
    """
    
    def __init__(self, num_layers: int, d_model: int, num_heads: int, d_ff: int):
        self.num_layers = num_layers
        self.d_model = d_model
        
        # Инициализация слоев декодировщика
        self.attention_layers1 = [MultiHeadAttention(d_model, num_heads) for _ in range(num_layers)]
        self.attention_layers2 = [MultiHeadAttention(d_model, num_heads) for _ in range(num_layers)]
        self.ffn_layers = [FeedForwardNetwork(d_model, d_ff) for _ in range(num_layers)]
        self.norm_layers = [LayerNormalization(d_model) for _ in range(num_layers)]
        
        
    def forward(self, x: np.ndarray, enc_output: np.ndarray, mask1: np.ndarray = None, mask2: np.ndarray = None) -> np.ndarray:
        """
        Прямой проход через декодировщик.
        На входе получаем матрицу с размером (batch_size, seq_len, d_model), которая является входными данными для декодировщика, а также выход кодировщика и необязательные маски.
        Мы последовательно проходим через каждый слой декодировщика, применяя механизм многоголового внимания (маскированного для первого внимания и не маскированного для второго внимания), слой нормализации, feed-forward сеть и снова слой нормализации для получения итогового представления.
        Это итоговое представление позволяет модели захватывать сложные зависимости в данных и улучшать способность к генерализации и пониманию текста.
        В дальнейшем это представление может быть использовано в других компонентах трансформера для дальнейшей обработки и генерации выходных данных.
        """
        for i in range(self.num_layers):
            # Многоголовое внимание (маскированное)
            attention_output1, _ = self.attention_layers1[i].forward(x, x, x, mask1)
            x = self.norm_layers[i].forward(x + attention_output1)  # Слой нормализации после первого внимания
            
            # Многоголовое внимание (не маскированное)
            attention_output2, _ = self.attention_layers2[i].forward(x, enc_output, enc_output, mask2)
            x = self.norm_layers[i].forward(x + attention_output2)  # Слой нормализации после второго внимания
            
            # Feed-forward сеть
            ffn_output = self.ffn_layers[i].forward(x)
            x = self.norm_layers[i].forward(x + ffn_output)  # Слой нормализации после feed-forward сети
            
        return x
    
    
class Transformer:
    """
    Класс для реализации трансформера, который объединяет кодировщик и декодировщик.
    Трансформер - это архитектура, которая позволяет моделям эффективно обрабатывать последовательности данных, такие как текст, и захватывать сложные зависимости в данных.
    """
    
    def __init__(
        self,
        num_layers: int,
        d_model: int,
        num_heads: int,
        d_ff: int
    ):
        self.encoder = Encoder(num_layers, d_model, num_heads, d_ff)
        self.decoder = Decoder(num_layers, d_model, num_heads, d_ff)
        
        
    def forward(self, enc_input: np.ndarray, dec_input: np.ndarray, enc_mask: np.ndarray = None, dec_mask: np.ndarray = None) -> np.ndarray:
        """
        Прямой проход через трансформер.
        На входе получаем матрицы с размером (batch_size, seq_len, d_model) для кодировщика и декодировщика, а также необязательные маски.
        Мы сначала пропускаем входные данные через кодировщик, а затем используем выход кодировщика в декодировщике для получения итогового представления.
        Это итоговое представление позволяет модели захватывать сложные зависимости в данных и улучшать способность к генерализации и пониманию текста.
        В дальнейшем это представление может быть использовано для генерации выходных данных, таких как предсказание следующего слова в последовательности или перевод текста на другой язык.
        """
        enc_output = self.encoder.forward(enc_input, enc_mask)
        dec_output = self.decoder.forward(dec_input, enc_output, dec_mask)
        
        return dec_output