# === TF-IDF ===

def compute_tfidf(bow_matrix: np.ndarray) -> np.ndarray:
    """Вычисление TF-IDF матрицы"""

    n_docs, n_words = bow_matrix.shape

    # TF = частоты (уже есть в bow_matrix, но нормируем по документу)
    # Считаем сумму слов в каждом документе
    doc_lengths = np.sum(bow_matrix, axis=1, keepdims=True)
    # Защита от деления на ноль
    doc_lengths[doc_lengths == 0] = 1
    tf = bow_matrix / doc_lengths

    # IDF: log( (число документов) / (число документов со словом) )
    # Считаем, в скольких документах есть каждое слово
    doc_freq = np.sum(bow_matrix > 0, axis=0)
    # Защита от деления на ноль
    doc_freq[doc_freq == 0] = 1
    idf = np.log(n_docs / doc_freq)

    tf_idf = tf * idf

    return tf_idf

