def tokenize(text: str) -> List[str]:
    """Разбивает текст на слова, приводит к нижнему регистру."""

    text = text.lower()
    words = re.findall(r'\b[а-яa-z]+\b', text)
    words = [w for w in words if len(w) >= 3]
    return words

