"""Подробно прокомментированные обучающие и демонстрационные функции для CNN."""

# Импортируем NumPy для генерации искусственных данных в демонстрационном сценарии.
import numpy as np
# Импортируем pyplot для визуализации карт признаков, чтобы обучение и работа фильтров были наглядными.
from matplotlib import pyplot as plt

# Импортируем основной класс CNN из выделенного базового модуля, чтобы разделить модель и сценарии ее использования.
from cnn_core import CNN


# Добавляем метод визуализации к классу CNN, сохраняя исходную функциональность, но вынося вспомогательную часть отдельно от ядра модели.
def visualize_feature_maps(self, X: np.ndarray):
    # Приводим вход к форме одного изображения, потому что свертка ожидает двумерный массив.
    X = X.reshape(self.input_shape)
    # Создаем сетку подграфиков: по одному окну для каждой карты признаков.
    fig, axes = plt.subplots(1, self.num_filters, figsize=(4 * self.num_filters, 4))
    # Если фильтр всего один, оборачиваем ось в список, чтобы единообразно обходить результат в цикле.
    if self.num_filters == 1:
        axes = [axes]
    # Перебираем фильтры модели, чтобы для каждого показать соответствующую карту признаков.
    for idx, current_filter in enumerate(self.filters):
        # Получаем карту признаков сверткой текущего фильтра по входному изображению.
        feature_map = self.convolve(X, current_filter)
        # Применяем ReLU, чтобы визуализация совпадала с тем, что реально идет дальше по сети.
        feature_map = self.relu(feature_map)
        # Берем текущую область вывода, соответствующую номеру фильтра.
        ax = axes[idx]
        # Отображаем карту признаков в тепловой палитре, чтобы сильные активации были лучше заметны.
        im = ax.imshow(feature_map, cmap='hot')
        # Подписываем график номером карты признаков, чтобы различать результаты разных фильтров.
        ax.set_title(f'Feature Map {idx + 1}')
        # Отключаем оси, потому что в данном учебном примере важнее структура активаций, а не координатная сетка.
        ax.axis('off')
        # Добавляем цветовую шкалу, чтобы было легче интерпретировать величину активаций.
        plt.colorbar(im, ax=ax)
    # Автоматически подбираем отступы между графиками, чтобы подписи не перекрывались.
    plt.tight_layout()
    # Показываем окно визуализации пользователю.
    plt.show()


# Добавляем метод одного шага обучения к классу CNN, чтобы логика использования была отделена от базовой математики модели.
def train_step(self, X: np.ndarray, y: np.ndarray):
    # Приводим пример к форме изображения, чтобы свертка инициализированного размера отработала корректно.
    X = X.reshape(self.input_shape)
    # Создаем список для карт признаков, которые появятся после обработки всеми фильтрами.
    outputs = []
    # Последовательно применяем каждый фильтр, полностью повторяя логику прямого прохода ядра модели.
    for current_filter in self.filters:
        # Выполняем свертку для очередного фильтра.
        output = self.convolve(X, current_filter)
        # Пропускаем полученную карту признаков через ReLU.
        output = self.relu(output)
        # Сохраняем результат, чтобы затем объединить карты признаков в общий тензор.
        outputs.append(output)
    # Объединяем карты признаков всех фильтров в единый массив.
    conv_output = np.stack(outputs)
    # Разворачиваем сверточный тензор в плоский вектор признаков для полносвязного слоя.
    flattened = conv_output.flatten()
    # Вычисляем логиты классов линейным преобразованием.
    logits = np.dot(flattened, self.dense_weights) + self.dense_bias
    # Считаем значение функции потерь для текущего примера.
    loss = self.compute_loss(logits, y)
    # Используем известный градиент softmax + cross-entropy по логитам для one-hot разметки.
    d_loss = self.softmax(logits) - y
    # Передаем градиент в обратный проход, чтобы обновить параметры модели.
    self.backward(d_loss, conv_output, X)
    # Возвращаем значение потерь, чтобы вызывающий код мог усреднять его по эпохе.
    return loss


# Определяем простой обучающий цикл для набора примеров, чтобы вынести высокоуровневую логику из класса.
def train(cnn: CNN, X_train: np.ndarray, y_train: np.ndarray, epochs: int = 10):
    # Запускаем внешний цикл по эпохам, потому что один и тот же набор данных обычно проходит через модель много раз.
    for epoch in range(epochs):
        # Обнуляем накопитель потерь в начале каждой эпохи.
        total_loss = 0
        # Перебираем входы и метки попарно, обучая модель на каждом примере по очереди.
        for X, y in zip(X_train, y_train):
            # Выполняем один обучающий шаг и получаем потери на этом примере.
            loss = cnn.train_step(X, y)
            # Добавляем потери к сумме, чтобы потом вычислить среднее значение по эпохе.
            total_loss += loss
        # Печатаем номер эпохи и среднюю потерю, чтобы можно было наблюдать динамику обучения.
        print(f'Epoch {epoch + 1}/{epochs}, Loss: {total_loss / len(X_train):.4f}')


# Привязываем вынесенные функции обратно к классу CNN, чтобы внешний интерфейс остался совместимым с исходным файлом.
CNN.visualize_feature_maps = visualize_feature_maps
# Привязываем функцию одного шага обучения как метод класса, сохраняя исходное имя и способ вызова.
CNN.train_step = train_step


# Определяем демонстрационный сценарий, который запускается только при прямом выполнении модуля.
def run_demo():
    # Сообщаем в консоль, что начинается демонстрационное тестирование сверточной сети.
    print('Тестирование CNN на случайных данных...')
    # Создаем экземпляр модели с теми же параметрами, что и в исходном примере.
    cnn = CNN(num_classes=10, num_filters=4, filter_size=3, input_shape=(28, 28))
    # Генерируем искусственный обучающий набор из ста случайных изображений размером 28 на 28.
    X_train = np.random.rand(100, 28, 28)
    # Визуализируем карты признаков для первого изображения, чтобы показать работу фильтров.
    cnn.visualize_feature_maps(X_train[0])
    # Генерируем случайные one-hot метки для десяти классов, сохраняя исходную демонстрационную логику.
    y_train = np.eye(10)[np.random.randint(0, 10, 100)]
    # Запускаем обучение на заданном числе эпох.
    train(cnn, X_train, y_train, epochs=50)
    # Сообщаем в консоль, что демонстрация завершилась.
    print('Тестирование завершено.')
